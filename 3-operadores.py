# Operadores Aritméticos
a = 5
b = 2

suma = a + b        # Suma
resta = a - b       # Resta
multiplicacion = a * b     # Multiplicación
division = a / b        # División
division_entera = a // b   # División entera (cociente)
modulo = a % b      # Módulo (resto de la división)
potencia = a ** b       # Potenciación (exponenciación)

print("Suma:", suma)
print("Resta:", resta)
print("Multiplicación:", multiplicacion)
print("División:", division)
print("División entera:", division_entera)
print("Módulo:", modulo)
print("Potencia:", potencia)

# Operadores de Comparación
x = 5
y = 10

igual = x == y      # Igualdad
desigual = x != y       # Desigualdad
menor_que = x < y       # Menor que
menor_o_igual_que = x <= y  # Menor o igual que
mayor_que = x > y       # Mayor que
mayor_o_igual_que = x >= y  # Mayor o igual que

print("Igualdad:", igual)
print("Desigualdad:", desigual)
print("Menor que:", menor_que)
print("Menor o igual que:", menor_o_igual_que)
print("Mayor que:", mayor_que)
print("Mayor o igual que:", mayor_o_igual_que)

# Operadores Lógicos
p = True
q = False

y_logico = p and q     # Y lógico
o_logico = p or q      # O lógico
negacion = not p       # Negación lógica

print("Y lógico:", y_logico)
print("O lógico:", o_logico)
print("Negación:", negacion)

# Operadores de Asignación
a = 5
b = 2

a += b      # Suma y asignación
print("a después de suma y asignación:", a)

a -= b      # Resta y asignación
print("a después de resta y asignación:", a)

a *= b      # Multiplicación y asignación
print("a después de multiplicación y asignación:", a)

a /= b      # División y asignación
print("a después de división y asignación:", a)

a //= b     # División entera y asignación
print("a después de división entera y asignación:", a)

a %= b      # Módulo y asignación
print("a después de módulo y asignación:", a)

a **= b     # Potenciación y asignación
print("a después de potenciación y asignación:", a)
